numero = input('Ingrese un numero: ')

if numero=='0':
    print('El numero es cero')
elif numero=='1':
    print('El numero es uno')
elif numero=='2':
    print('El numero es dos')
elif numero=='3':
    print('El numero es tres')
elif numero=='4':
    print('El numero es cuatro')
else:
    print('No se que numero es')